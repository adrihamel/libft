#include "libft.h"

size_t	ft_strlen(const char *s)
{
	int	n;

	if (s == NULL)
		return (0);
	n = 0;
	if (s)
	{
		while (*s != '\0')
		{
			n++;
			s++;
		}
	}
	return (n);
}
